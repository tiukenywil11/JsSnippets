// Functions

// Declaring a function
function addNums(num1, num2) {
    return num1 + num2;
}
// Calling a function
addNums(5,4);
// Show value for the return of the function
console.log(addNums(5,4));


// Arrow function
const addNums_arrow = (num1_arrow = 1, num2_arrow = 1) => num1_arrow + num2_arrow;
console.log(addNums_arrow(5,5));




